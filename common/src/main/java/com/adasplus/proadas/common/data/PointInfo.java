public class PointInfo {


    /**
     * ret : 1000
     * x : 338.99997
     * y : 442.99973
     */

    private String ret;
    private String x;
    private String y;

    public String getRet() {
        return ret;
    }

    public void setRet(String ret) {
        this.ret = ret;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }
}
